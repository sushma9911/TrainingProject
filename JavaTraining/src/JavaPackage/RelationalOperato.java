package JavaPackage;

public class RelationalOperato {

	public static void main(String[] args) {
		
		int a = 300;
		int b = 200;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);

	}

}
