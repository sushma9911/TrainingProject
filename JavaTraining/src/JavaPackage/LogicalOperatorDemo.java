package JavaPackage;

public class LogicalOperatorDemo {
	public static void main(String[] args) {
		
		
		int a=10;//Local Variable
		int b=5;//Local Variable
		int c=20;//Local Variable
		
		//Logical Operator
		System.out.println(a < b && a < c);//false
		
		//BitWise Operator
		System.out.println(a < b & a < c);//false
	}

}
