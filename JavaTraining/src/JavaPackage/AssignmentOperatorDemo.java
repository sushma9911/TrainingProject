package JavaPackage;

public class AssignmentOperatorDemo {

	public static void main(String[] args) {
		
		int a=10;
		
		a +=3;// a=10+3=13
		System.out.println(a);
		
		//Do Not Give Space between + and =
		
		a -=4;
		System.out.println(a);//a=13-4=9
		
		a *=2;
		System.out.println(a);//18
		
		a /=2;
		System.out.println(a);//9

	}

}
